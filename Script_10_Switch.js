
var digit = 4;

switch (digit) {
    case 0:
        console.log("\n 0 - Zero");
        break;
    case 1:
        console.log("\n 1 - One");
        break;
    case 2:
        console.log("\n 2 - Two");
        break;
    case 3:
        console.log("\n 3 - Three");
        break;
    case 4:
        console.log("\n 4 - Four");
        break;
    case 5:
        console.log("\n 5 - Five");
        break;
    case 6:
        console.log("\n 6 - Six");
        break;
    case 7:
        console.log("\n 7 - Seven");
        break;
    case 8:
        console.log("\n 8 - Eight");
        break;
    case 9:
        console.log("\n 9 - Nine");
        break;

    default:
        console.log("\nInvalid digit!!");
}